        <p>保证金是号码直通车商家对外展示的一个诚信标志，交过保证金的商家给一个保证标：<img src="/uploads/images/benbenStore/guarantee.png" alt="guarantee LOGO" style="display: inline-block;">，并签订一份保证协议，保证在奔犇上交易的诚信，具体规则如下：</p>


        <p>一.收保证金标准，现在针对不同的行业收取不同的保证金，即每个行业暂定一个保证金的金额，具体如下：1000、3000、10000；</p>

        <p>二.会员号商家一定要交保证金，保证金的收取办法同上</p>

        <p>三.所有号直商家都可以申请成为保证金商家，保证金收取办法同上</p>

        <p>四.退保证金办法，交保证金都有一个协议，用户协议到期后可以申请退，正常情况下30日内退给商家；
        <ul style="list-style-type: decimal;">
            <li>会员号商家不能退</li>
            <li>会员号商家取消会员号后可以申请退出，要在会员号业务到期后才能申请</li>
            <li>其他商家申请退出要在最后一笔交易后的15个工作日才能申请</li>
        </ul>
        </p>

        <p>五.保证金使用规则：用户对商家进行投诉时，商家要及时处理，处理不好，我们有权使用保证金给用户处理。</p>


