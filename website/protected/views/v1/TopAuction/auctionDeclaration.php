        <p>号码直通车置顶功能，将使您的店铺拥有更强的展位技能，让您的商品或服务在同地区或同行业中优先展示，抢占前三条，拼的就是速度！</p>

        <h2>1、置顶拍卖项目</h2>

        <p>号码直通车置顶形式有两种：区域置顶和区域+行业置顶。区域置顶可以选择以区县为基本单位；区域+行业置顶可以选区县+行业。</p>

        <p>奔犇将根据实际运营需要，逐步释放置顶项目，只有释放到拍卖平台上的置顶项目，您才可以拍卖。</p>

        <h2>2、置顶拍卖时效</h2>

        <p>所有释放到拍卖平台上的置顶项目，都有拍卖时效，时效详见拍卖平台；只有在时效期内，您才可以进行加价竞拍。</p>

        <h2>3、置顶项目拍卖规则</h2>

        <p>奔犇将根据不同号码直通车置顶项目设置起拍价格和每轮加价最低金额，您可以根据最新价格设置合理的加价金额进行拍卖，在拍卖时效截止时，竞价最高者得之。起拍价和加价金额详见置顶拍卖项目。</p>

        <h2>4、竞拍资金管理</h2>

        <p>您可以根据需要，选择不同置顶项目进行一轮或者多轮竞拍，每参与一个置顶项目竞拍，将自动从您账户中冻结100元作为竞拍保证金，保证金在未解除冻结状态前，不能提现。</p>

        <p>
            置顶项目竞拍结束后，未拍得项目的竞拍参与者，将自动解冻保证金；置顶项目竞得者，中标的用户如果中标价不到100元，那么自动扣款，余额解冻；中标价若大于100，那么先扣除100元，剩下金额需要在3天内补齐，若不补齐，没收保证金，本次拍卖流拍。</p>

        <h2>5、充值入口</h2>

        <p>奔犇平台开放了支付宝充值接口，您可以通过支付宝对您的账户进行充值。</p>

